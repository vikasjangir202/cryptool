			<ol>
<li><b>Symmetric Key Cryptography:</b></li><br>
It is an encryption system where the sender and receiver of message use a single common<br> key to encrypt and encrypt messages. Symmetric Key Systems are faster and <br>simpler but the problem is that sender and receiver have to somehow exchange key is a <br>secure manner. The most popular symmetric key cryptography system is Data Encryption System(DES).<br><br>

<li><b>Hash Functions:</b></li><br>
There is no usage of any key in this algorithm. A hash value with fixed length is <br>calculated as per the plain text which makes it impossible for contents of plain text <br>to be recovered. Many operating systems use hash functions to encrypt <br>passwords.<br><br>

<li><b>Asymmetric Key Cryptography:</b></li><br>
Under this system a pair of keys is used to encrypt and decrypt information. A public key <br>is used for encryption and a private key is used for decryption. Public key and <br>Private Key are different. Even if the public key is known by everyone the intended <br>receiver can only decode it because he alone knows the private key.<br><br>
