	<font size=5><p>Cryptography is technique of securing information and communications through <br>use of codes so that only those person for whom the information is intended can <br>	understand it and process it. Thus preventing unauthorized access to information.<br> 	The prefix “crypt” means “hidden” and suffix graphy means “writing”.</p>
<br>
	<p>In Cryptography the techniques which are use to protect information are <br>obtained from mathematical concepts and a set of rule based calculations known as 	<br>algorithms to convert messages in ways that make it hard to decode it. These 	<br>algorithms are used for cryptographic key generation, digital signing, 		<br>verification to protect data privacy, web browsing on internet and to protect 	<br>confidential transactions such as credit card and debit card transactions.
